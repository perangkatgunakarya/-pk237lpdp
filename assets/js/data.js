function tampilkanNamaDalamKelompok(dataGroup, sizeGroup, numberGroup) {
    const container = document.getElementById(numberGroup);

    for (let i = 0; i < dataGroup.length; i += sizeGroup) {
        // Membuat elemen div untuk setiap kelompok
        const kelompokDiv = document.createElement('div');
        kelompokDiv.classList.add('flex', 'flex-col', 'gap-4'); // Menambahkan kelas untuk kelompok

        for (let j = i; j < i + sizeGroup && j < dataGroup.length; j++) {
            // Membuat elemen div untuk setiap nama dalam kelompok
            const namaDiv = document.createElement('div');
            namaDiv.textContent = dataGroup[j];
            // Menambahkan elemen div nama ke dalam kelompok
            kelompokDiv.appendChild(namaDiv);
        }

        // Menambahkan elemen div kelompok ke dalam container
        container.appendChild(kelompokDiv);
    }
}

